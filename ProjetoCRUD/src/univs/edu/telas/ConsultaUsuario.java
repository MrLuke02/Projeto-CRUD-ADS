    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int linha = tabelaUsuario.getSelectedColumn();
        if(linha == -1){
            JOptionPane.showMessageDialog(null, "Selecione uma linha!");
        }else if(JOptionPane.showConfirmDialog(null, "Deseja realmente deletar esse usu√°rio?", "Excluir", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
            usuario = dao.pesquisar((int)tabelaUsuario.getValueAt(linha, 0));
            dao.excluir(usuario);
            JOptionPane.showMessageDialog(null, "Excluido com sucesso!");
            AtualizarTabela();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int linha = tabelaUsuario.getSelectedColumn();
        if(linha == -1){
            JOptionPane.showMessageDialog(null, "Selecione uma linha!");
        }else{
            usuario = dao.pesquisar((int)tabelaUsuario.getValueAt(linha, 0));
        TelaUsuario cadastro = new TelaUsuario();
        cadastro.usuario = usuario;
        cadastro.atualizarCampos();
        cadastro.setVisible(true);
            
    }//GEN-LAST:event_jButton3ActionPerformed
    }
